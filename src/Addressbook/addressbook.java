    private void updateTable() {
        DefaultTableModel modal = (DefaultTableModel)jTable1.getModel();
        modal.setRowCount(0);
        for(data d:addressbook){
          modal.addRow(new Object[]{d.getId(), d.getPname(), d.getPhoneno(), d.getBphoneno(), d.getFax(), d.getCphone(), d.getPager(), d.getHadd(), d.getBadd()});
        }
        jTable1.setModel(modal);
    }
    
