    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        addressbook.add(new data(Integer.parseInt(jTextField7.getText()), jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jTextField5.getText(), jTextField6.getText(), jTextArea1.getText(), jTextArea2.getText()));
        updateTable();
        clearform();
    }//GEN-LAST:event_jButton1ActionPerformed

