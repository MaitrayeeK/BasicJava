    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jTextField7.getText());
        Iterator<data> it = addressbook.iterator();
        if(it.hasNext()) {
            data d = it.next();
            if(d.getId() == id) {
                addressbook.remove(addressbook.indexOf(d));
            }
        }
        updateTable();
        clearform();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel modal = (DefaultTableModel)jTable1.getModel();
        jTextField7.setText(modal.getValueAt(jTable1.getSelectedRow(), 0).toString());
        jTextField1.setText(modal.getValueAt(jTable1.getSelectedRow(), 1).toString());
        jTextField2.setText(modal.getValueAt(jTable1.getSelectedRow(), 2).toString());
        jTextField3.setText(modal.getValueAt(jTable1.getSelectedRow(), 3).toString());
        jTextField4.setText(modal.getValueAt(jTable1.getSelectedRow(), 4).toString());
        jTextField5.setText(modal.getValueAt(jTable1.getSelectedRow(), 5).toString());
        jTextField6.setText(modal.getValueAt(jTable1.getSelectedRow(), 6).toString());
        jTextArea1.setText(modal.getValueAt(jTable1.getSelectedRow(), 7).toString()); 
        jTextArea2.setText(modal.getValueAt(jTable1.getSelectedRow(), 8).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        for(data d:addressbook){
            if(Integer.parseInt(jTextField7.getText()) == d.getId()) {
                addressbook.set(addressbook.indexOf(d), new data(Integer.parseInt(jTextField7.getText()), jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jTextField5.getText(), jTextField6.getText(), jTextArea1.getText(), jTextArea2.getText()));
            }
        }
        updateTable();
        clearform();
    }//GEN-LAST:event_jButton3ActionPerformed

